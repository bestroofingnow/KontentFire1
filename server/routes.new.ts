  app.get('/api/subscription', async (req: Request, res: Response) => {
    if (!req.isAuthenticated()) {
      return res.status(401).json({ message: 'Not authenticated' });
    }
    
    if (!stripe) {
      return res.status(503).json({ message: 'Stripe integration not available' });
    }
    
    try {
      const user = req.user;
      
      if (!user.stripeSubscriptionId) {
        return res.json({
          status: 'no-subscription',
          plan: user.plan || 'free',
          planStatus: user.planStatus || 'inactive',
        });
      }
      
      const subscription = await stripe.subscriptions.retrieve(user.stripeSubscriptionId);
      
      return res.json({
        status: subscription.status,
        currentPeriodEnd: new Date(subscription.current_period_end * 1000),
        cancelAtPeriodEnd: subscription.cancel_at_period_end,
        plan: user.plan,
        planStatus: user.planStatus,
      });
    } catch (error: any) {
      console.error('Error fetching subscription:', error);
      res.status(500).json({ message: `Error fetching subscription: ${error.message}` });
    }
  });
  
